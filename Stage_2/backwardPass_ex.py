import torch
import torch.nn as nn

# ==============================================
# 📌 학습 루프란?
# 순전파 → Loss → 역전파 → 업데이트
# 이걸 수천 번 반복하면 모델이 점점 똑똑해짐!
# ==============================================


# ==============================================
# 1. 모델 정의
# ==============================================
model = nn.Sequential(
    # 입력 4개 → 숨겨진 8개 뉴런
    nn.Linear(4, 8),
    nn.ReLU(),
    # 숨겨진 8개 → 출력 3개 (클래스 3개)
    nn.Linear(8, 3),
)


# ==============================================
# 2. Loss 함수 정의
# ==============================================
# CrossEntropyLoss = Softmax + Cross-Entropy 한 번에!
# 분류 문제에서 가장 많이 쓰는 Loss
criterion = nn.CrossEntropyLoss()


# ==============================================
# 3. Optimizer 정의
# ==============================================
# Optimizer = 가중치를 어떻게 업데이트할지 결정
# SGD = 가장 기본적인 방법
# lr = learning rate (학습률 η, 한 걸음 크기)
optimizer = torch.optim.SGD(
    model.parameters(),  # 업데이트할 가중치들
    lr=0.1               # 학습률
)


# ==============================================
# 4. 학습 데이터 준비
# ==============================================
# 실제로는 수천~수백만 개의 데이터를 사용
# 여기서는 간단하게 5개만 사용
x = torch.randn(5, 4)
# 입력: 5개 데이터, 각 특징 4개
# ex) 고양이 사진 5장의 특징 벡터

y = torch.tensor([0, 1, 2, 0, 1])
# 정답 라벨: 각 데이터의 클래스
# 0=고양이, 1=강아지, 2=새


# ==============================================
# 5. 학습 루프
# ==============================================
print("=" * 55)
print("학습 시작!")
print("=" * 55)
print(f"{'Step':>4} | {'Loss':>8} | {'설명'}")
print("-" * 55)

for step in range(30):  # 30번 반복

    # -----------------------------------------------
    # ① 순전파 (Forward Pass)
    # 입력 데이터를 모델에 넣어서 예측값 계산
    # -----------------------------------------------
    pred = model(x)
    # pred.shape = (5, 3)
    # 5개 데이터 × 3개 클래스 점수

    # -----------------------------------------------
    # ② Loss 계산
    # 예측값과 정답을 비교해서 벌점 계산
    # Loss 높음 = 많이 틀림
    # Loss 낮음 = 잘 맞춤
    # -----------------------------------------------
    loss = criterion(pred, y)

    # -----------------------------------------------
    # ③ 그래디언트 초기화
    # ⚠️ 매우 중요! 안 하면 이전 그래디언트 누적됨
    # PyTorch는 기본적으로 그래디언트를 더하기 때문
    # -----------------------------------------------
    optimizer.zero_grad()

    # -----------------------------------------------
    # ④ 역전파 (Backward Pass)
    # Loss를 기준으로 각 가중치의 그래디언트 자동 계산
    # 연쇄 법칙을 자동으로 적용!
    # -----------------------------------------------
    loss.backward()

    # -----------------------------------------------
    # ⑤ 가중치 업데이트
    # W = W - lr × gradient
    # 각 가중치를 Loss가 줄어드는 방향으로 조금씩 수정
    # -----------------------------------------------
    optimizer.step()

    # 5 스텝마다 결과 출력
    if (step + 1) % 5 == 0:
        # 현재 예측 정확도 계산
        correct = (pred.argmax(dim=1) == y).sum().item()
        accuracy = correct / len(y) * 100
        print(f"{step+1:>4} | "
              f"{loss.item():>8.4f} | "
              f"정확도: {accuracy:.0f}% "
              f"({correct}/{len(y)} 정답)")

print("=" * 55)
print("학습 완료!")
print()


# ==============================================
# 6. 학습 전후 비교
# ==============================================
print("=" * 55)
print("📌 학습 전후 예측 비교")
print("=" * 55)

# 학습 전 (랜덤 모델)
fresh_model = nn.Sequential(
    nn.Linear(4, 8),
    nn.ReLU(),
    nn.Linear(8, 3),
)
before_pred = fresh_model(x).argmax(dim=1)

# 학습 후 (우리 모델)
after_pred = model(x).argmax(dim=1)

print(f"정답:      {y.tolist()}")
print(f"학습 전:   {before_pred.tolist()}  ← 랜덤 예측")
print(f"학습 후:   {after_pred.tolist()}   ← 학습된 예측")
print()


# ==============================================
# 7. 그래디언트 직접 확인
# ==============================================
print("=" * 55)
print("📌 그래디언트 확인 (역전파의 핵심)")
print("=" * 55)

# 간단한 예시로 그래디언트 직접 보기
w = torch.tensor(3.0, requires_grad=True)
# requires_grad=True = "이 값의 그래디언트를 추적해줘"

# f(w) = w² (Loss라고 생각)
loss_simple = w ** 2

# 역전파: df/dw = 2w = 2×3 = 6
loss_simple.backward()

print(f"w = {w.item()}")
print(f"Loss = w² = {loss_simple.item()}")
print(f"그래디언트 = dLoss/dw = 2w = {w.grad.item()}")
print(f"  → w=3 일 때 기울기는 6")
print(f"  → 이 방향의 반대로 w를 수정해야 Loss가 줄어듦!")
print()
print(f"업데이트 후 w:")
print(f"  w_new = w - lr × gradient")
print(f"  w_new = {w.item()} - 0.1 × {w.grad.item()}")
print(f"  w_new = {w.item() - 0.1 * w.grad.item():.1f}")
print(f"  (3.0 → 2.4로 줄어듦, 정답 0에 가까워짐!)")